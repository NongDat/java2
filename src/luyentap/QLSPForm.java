    @Override
    public void run() {
        while(true){
            try {
                SimpleDateFormat formater = new SimpleDateFormat("hh:mm:ss aa");
                Date date = new Date();
                String time = formater.format(date);
                lblT.setText(time);
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                ex.printStackTrace();
                break;
            }
        }
    }
    public void chuChay(){
        chuChay = new Thread(){
            String txt = lbltt.getText();
            public void run(){
                while(true){
                    try {
                        txt = txt.substring(1,txt.length()) + txt.charAt(0);
                        lbltt.setText(txt);
                        Thread.sleep(120);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                        break;
                    }
                }
            }
        };chuChay.start();
    }
