    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(!this.checkTT()){
            return;
        }
        String tenSP = this.txtTen.getText(),
                maSP = this.txtMa.getText(),
                loai = this.cbbLoai.getSelectedItem().toString();
        int tinhTrang = this.rdoCon.isSelected() ? 1 : 0;
        double gia = Double.parseDouble(this.txtGia.getText());
        Product sp = new Product(tenSP, maSP, loai, tinhTrang, gia);
        this.qlds.them(sp);
        this.fillToTable();
        JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = this.tblSP.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn vị trí cần sửa !");
            return;
        }
        if(!this.checkTT()){
            return;
        }
        String tenSP = this.txtTen.getText(),
                maSP = this.txtMa.getText(),
                loai = this.cbbLoai.getSelectedItem().toString();
        int tinhTrang = this.rdoCon.isSelected() ? 1 : 0;
        double gia = Double.parseDouble(this.txtGia.getText());
        Product sp = new Product(tenSP, maSP, loai, tinhTrang, gia);
        this.qlds.sua(index, sp);
        this.fillToTable();
        JOptionPane.showMessageDialog(this, "Sửa thành công !");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = this.tblSP.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn vị trí cần xóa !");
            return;
        }
        int YN = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?");
        if (YN == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Đã hủy xóa!");
            return;
        } else if (YN == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Đã hủy xóa!");
            return;
        } else if (YN == JOptionPane.YES_OPTION) {
            this.qlds.xoa(index);
            this.fillToTable();
            JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

